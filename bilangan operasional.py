# Ambil input untuk mengisi nilai
a = input("Inputkan nilai a: ")
b = input("Inputkan nilai b: ")

a = int(a)
b= int(b)

# Menggunakan operator penjumlahan
c = a + b
print ("Hasil %d + %d = %d" % (a,b,c))

# Operator Pengurangan
c = a - b
print ("Hasil %d - %d = %d" % (a,b,c))

# Operator Perkalian
c = a * b
print ("Hasil %d * %d = %d" % (a,b,c))

# Operator Pembagian
c = a / b
print ("Hasil %d / %d = %d" % (a,b,c))

# Operator Sisa Bagi
c = a % b
print ("Hasil %d %% %d = %d" % (a,b,c))

# Operator Pangkat
c = a ** b
print ("Hasil %d ** %d = %d" % (a,b,c))

#fungsi % dalam formatting

# %s untuk str format
# %d untuk int format
# %f untuk float format